#include "stm32f30x.h"
#include "rng.h"

#include "stdlib.h"

void RNG_Init(void)
{
	srand(SysTick->VAL);
}

uint16_t RNG_GetValue(void)
{
int val;
	
	val = rand();
	return val;
}


